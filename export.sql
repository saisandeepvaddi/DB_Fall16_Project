    (SELECT X.LNAME FROM EMPLOYEE X, DEPARTMENT Y WHERE X.SSN = Y.MGRSSN AND Y.DNO = E.DNO) AS MGR_LNAME,
    (SELECT J.SALARY FROM EMPLOYEE J, DEPARTMENT Z WHERE J.SSN = Z.MGRSSN AND Z.DNO = E.DNO) AS MGR_SALARY,
    (SELECT AVG(A.SALARY) FROM EMPLOYEE A WHERE A.DNO = E.DNO) DEPT_AVG_SAL
    FROM EMPLOYEE E;
