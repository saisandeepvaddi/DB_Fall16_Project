  CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##SANDEEP"."DEPT_DETAILS" ("DNAME", "FNAME", "LNAME", "NO_OF_EMP", "NO_OF_PROJ") AS 
  SELECT D.DNAME, M.FNAME, M.LNAME, 
      (SELECT COUNT(E.SSN) FROM EMPLOYEE E WHERE E.DNO = D.DNO) AS NO_OF_EMP, 
      (SELECT COUNT(P.PNO) FROM PROJECT P WHERE P.DNO = D.DNO) AS NO_OF_PROJ
    FROM DEPARTMENT D, EMPLOYEE M
    WHERE M.SSN = D.MGRSSN;
